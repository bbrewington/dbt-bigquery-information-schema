SELECT
  j.JOB_ID,
  j.CREATION_DTTM_TZ as JOB_CREATION_DTTM_TZ,
  t.PROJECT_ID,
  t.DATASET_ID as DATASET_NAME,
  t.TABLE_ID as TABLE_NAME,
  CONCAT('`', t.PROJECT_ID, t.DATASET_ID, t.TABLE_ID, '`') as TABLE_FULL_ID
FROM {{ ref('stg__info_schema__jobs') }} as j
CROSS JOIN UNNEST(REFERENCED_TABLES) AS t
WHERE j.JOB_TYPE = 'QUERY'
